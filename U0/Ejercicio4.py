# Ejercicio 4:  Implementa un programa Python que solicite al usuario una cadena de caracteres (String) y muestre por pantalla la longitud de esta.

# Solicitar al usuario que ingrese una cadena
cadena = input("Introduce una cadena: ")

# Calcular la longitud de la cadena utilizando la funci√≥n len()
longitud_cadena = len(cadena)

# Imprimir la longitud de la cadena
print("La longitud de la cadena introducida es:", longitud_cadena)
